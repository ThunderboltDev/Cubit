import { Button as ButtonPrimitive } from "@base-ui/react/button";
import { cva, type VariantProps } from "class-variance-authority";
import type { ComponentProps } from "react";
import { cn } from "@/lib/utils";

const baseClasses = [
  "inline-flex items-center justify-center gap-1.5 shrink-0",
  "transition-all duration-200 ease-in-out",
  "focus-ring shadow-sm",
  "text-[15px] font-medium whitespace-nowrap",
  "hover:brightness-105 active:brightness-85",
  "rounded-md no-underline cursor-pointer",
  "[&_svg]:shrink-0 [&_svg]:pointer-events-none",
  "[&_svg:not([class*='size-'])]:size-4.5",
  "aria-invalid:ring-danger/50 aria-invalid:border-danger",
  "aria-busy:opacity-75 aria-busy:saturate-100 aria-busy:cursor-progress",
  "disabled:opacity-75 disabled:saturate-0 disabled:cursor-not-allowed",
];

const buttonVariants = cva(baseClasses.join(" "), {
  variants: {
    variant: {
      default:
        "bg-background text-foreground hover:brightness-100 hover:bg-secondary focus-visible:ring-foreground/50",
      secondary:
        "bg-secondary text-secondary-foreground hover:brightness-100 hover:bg-muted focus-visible:ring-foreground/50",
      outline:
        "bg-secondary text-secondary-foreground hover:brightness-100 hover:bg-muted focus-visible:ring-foreground/50 border border-border",
      ghost: "bg-transparent hover:bg-white/5 shadow-none",
      accent: "bg-accent text-white",
      danger: "bg-danger text-white focus-visible:ring-danger/50",
      success: "bg-success text-white focus-visible:ring-success/50",
      warning: "bg-warning text-white focus-visible:ring-warning/50",
    },
    size: {
      default:
        "h-9 px-4 py-2 has-[>svg]:px-3 [&_svg:not([class*='size-'])]:size-4.5",
      sm: "h-8.5 gap-1 px-3 text-sm has-[>svg]:px-3 [&_svg:not([class*='size-'])]:size-4",
      lg: "h-9.5 px-4 gap-2 text-base [&_svg:not([class*='size-'])]:size-5",
      icon: "size-9",
      "icon-sm": "size-8",
      "icon-lg": "size-10",
      responsive: [
        "h-9 px-4 py-2 has-[>svg]:px-3 [&_svg:not([class*='size-'])]:size-4.5",
        "md:h-8 md:gap-1 md:px-3 md:text-sm md:has-[>svg]:px-3",
      ],
    },
  },
  defaultVariants: {
    variant: "default",
    size: "default",
  },
});

type ButtonProps = ComponentProps<typeof ButtonPrimitive> &
  VariantProps<typeof buttonVariants>;

function Button({
  className,
  children,
  variant = "default",
  size = "default",
  ...props
}: ButtonProps) {
  return (
    <ButtonPrimitive
      data-slot="button"
      className={cn(buttonVariants({ variant, size, className }))}
      {...props}
    >
      {children}
    </ButtonPrimitive>
  );
}

export { Button };
